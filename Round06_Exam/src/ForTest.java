
public class ForTest {
	public static void main(String[] ar) {
		for (int x = 1; x <= 5; ++x) {
			System.out.println("*");
		}
		System.out.println("End");
		//for (초기화구문; 논리형조건식; 증감식) 
		//초기값 설정 후 논리형조건식에서 true이면 내용부 실행. 이후 증감식으로 올라와 증감.
		//다시 논리혀조건식으로 와서 true/false 따져. true일 경우 반복.
		//false일 경우 내용부에서 빠져나와. 
	}
	
}
